let x = +prompt('Введіть число 1, 0 або -3');
switch (x) {
    case 0:
        console.log("Невірно");
        break;
        case 1:
            console.log("Вірно");
            break;
            case -3:
                console.log("Вірно");
                break;
                default:
                    console.log ("Шо ти вводиш!?")
}

