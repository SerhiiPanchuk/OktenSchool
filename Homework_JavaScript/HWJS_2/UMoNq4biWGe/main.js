let day = 0;
if (day >= 1 && day <= 10) {
    console.log("Це перша декада місяця");
} else if (day > 10 && day <= 20) {
    console.log("Це друга декада місяця");
} else if (day > 20 && day <= 31) {
    console.log("Це третя декада місяця");
} else {
    console.log("Це невідома науці декада місяця");
}
